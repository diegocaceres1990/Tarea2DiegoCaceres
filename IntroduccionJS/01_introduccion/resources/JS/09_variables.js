/**               VARIABLES     
 * 
 * En programación, una variable es un contenedor para un valor.
 * Puedes pensar en las variables como pequeños contenedores
 * de información que se almacenan en la memoria de la computadora.
 * 
 * La información almacenada en una variable puede ser de varios tipos,
 * como números, strings, booleanos, etc. La información puede ser
 * un nombre se usuario, un número de teléfono, una dirección de correo.
 * 
 * Las variables también proporcionan una forma de etiquetar datos con
 * un nombre descriptivo, por lo que nuestro código es más fácil de leer,
 * de esa manera podemos recordar qué hace cada variable, nosotros y otro
 * programador que lea nuestro código.
 * 
 * En resumen, las variables etiquetan y almacenan datos en la memoria.
 * 
 * Solo hay unas cosas que se pueden hacer con las variables:
 * 1. Crear una variable con un nombre descriptivo.
 * 2. Almacenar o actualizar información almacenada en una variable.
 * 3. Recuperar información de la variable para su uso.
 * 
 * Es importante recordar que las variables son sensibles a las mayúsculas.
 * no pueden tener espacios.
 * 
 * Debes distinguir que las variables no son valores, contienen valores y los
 * representan con un nombre.  Los valores son los datos reales que se almacenan
 * en la memoria.
 * 
 * Para crear una variable en JavaScript, utilizamos la palabra clave var,
 * seguida de un nombre descriptivo de la variable. Luego, utilizamos el
 * signo igual = para asignar un valor a la variable. Por último, escribimos
 * el valor que queremos almacenar en la variable.
 */