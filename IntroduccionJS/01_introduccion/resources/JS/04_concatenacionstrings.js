/**          CONCATENACIÓN DE CADENAS         
 * 
 * La concatenación de cadenas es la unión de dos o más cadenas de texto.
 * Se agrega la cadena dercha a la cadena izquierda.  En JavaScript, la 
 * concatenación de cadenas se realiza con el operador +..
 * 
 */
console.log('Hola' + 'mundo'); //Imprime 'Hola mundo'
console.log('Hola' + ' ' + 'mundo'); //Imprime 'Hola mundo'
console.log('Hola' + ' ' + 'mundo' + '!'); //Imprime 'Hola mundo!'
console.log('Hola, ' + '¿cómo estás?'); //Imprime 'Hola, ¿cómo estás?'

/**Este proceso de agregar una cadena a otra se llama concatenación.
 * 
 * Observemos que en el segundo console.log() hay un espacio entre las 
 * comillas console.log('Hola' + ' ' + 'mundo');.  Si no hubiera un espacio
 * entre las comillas, las dos cadenas se imprimirían juntas sin espacio.
 *  
 * Al igual que las matemáticas, la concatenación de cadenas se realiza
 * de izquierda a derecha.  Siempre se agrega la cadena de la derecha
 * a la cadena de la izquierda.
 * 
 * Como en matemáticas normales, podemos combinar o encadenar 
 * nuestras operaciones para obtener un resultado final.
 */
console.log('uno'+ ' +' + ' uno' + ' = ' + 'dos'); //Imprime 'uno + uno = dos'