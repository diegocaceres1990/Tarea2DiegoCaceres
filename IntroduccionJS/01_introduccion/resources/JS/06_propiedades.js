/**              PROPIEDADES           
 * 
 * Cuando se introduce un nuevo fragmento de datos en un programa JavaScript,
 * el navegador lo guarda como una instancia del tipo de datos que es.
 * 
 * Una instancia es un único caso de algo.  Por ejemplo, 'Hola' es una 
 * instancia de un string, y 42 es una instancia de un número.
 * 
 * JavaScript automáticamente asigna propiedades a estas instancias.
 * 
 * Las propiedades son valores asociados con un objeto JavaScript.
 * 
 * Los tipos de datos tienen acceso a propiedades específicas que se
 * transmiten a cada instancia.  Por ejemplo, cada instancia de un string
 * tiene una propiedad llamada length que almacena el número de caracteres
 * en ese string.
 * 
 * Puede recuperar información de la propiedad agregando un punto y el
 * nombre de la propiedad al final de la string.
 */

console.log('Hola'.length); //Imprime 4
console.log('Hola, mundo'.length); //Imprime 11

/** el . (punto) es otro operador, lo llamaremos operador de punto,
 * un operador de punto se utiliza para acceder a las propiedades de un
 * valor.
 */

console.log('Aprendiendo a codificar'.length); //Imprime 23
