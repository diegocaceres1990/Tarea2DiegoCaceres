/**             Interpolación de cadenas
 * 
 * La interpolación de cadenas es una forma más moderna  de concatenar cadenas.
 * Nos permite insertar variables y expresiones directamente en una cadena 
 * utilizando ${}.  La interpolación de cadenas es más legible que la 
 * concatenación de cadenas y es más fácil de usar. La interpolación de
 * cadenas es una característica de ES6.
 * 
 * Para interpolar una cadena, colocamos la cadena entre comillas invertidas
 * (``) en lugar de comillas simples ('') o dobles ("").  Luego, colocamos
 * la variable o expresión que queremos interpolar entre ${} dentro de la
 * cadena.  La variable o expresión se evalúa y se convierte en una cadena
 * antes de ser interpolada en la cadena principal. La cadena resultante
 * se imprime en la consola.
 */
const mypet = 'perro';
console.log(`Mi mascota es un ${mypet}`); //Imprime 'Mi mascota es un perro'
